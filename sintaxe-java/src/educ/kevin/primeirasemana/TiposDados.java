package educ.kevin.primeirasemana;

public class TiposDados {
  public static void main(String[] args) {

    //Tipos de Strings para textos
    String meuNome = "Kevyn Rodrigues";
    System.out.println("Meu nome completo: " + meuNome);

    //Tipos de dados
    byte idade = 123;
    short ano = 2002;
    int cep = 56200000;
    float pi = 3.14F;
    double salario = 1275.33;
    System.out.println("Minha idade: " + idade + "\nAno de Nascimento: " + ano + "\nMeu CEP: " + cep + "\nNúmero PI: " + pi + "\nMeu salário: " + salario); //Quebra de linha "\n"

  }
}
